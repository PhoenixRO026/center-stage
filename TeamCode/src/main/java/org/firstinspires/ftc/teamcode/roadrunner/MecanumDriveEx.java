        imuStartHeading = pose.heading.toDouble();

        LynxFirmware.throwIfModulesAreOutdated(hardwareMap);

        for (LynxModule module : hardwareMap.getAll(LynxModule.class)) {
            module.setBulkCachingMode(LynxModule.BulkCachingMode.AUTO);
        }

        // TODO: make sure your config has motors with these names (or change them)
        //   see https://ftc-docs.firstinspires.org/en/latest/hardware_and_software_configuration/configuring/index.html
        /*leftFront = hardwareMap.get(DcMotorEx.class, "leftFront");
        leftBack = hardwareMap.get(DcMotorEx.class, "leftBack");
        rightBack = hardwareMap.get(DcMotorEx.class, "rightBack");
        rightFront = hardwareMap.get(DcMotorEx.class, "rightFront");*/
        leftFront = MotorEx.Companion.rev12to1(hardwareMap.get(DcMotorEx.class, "leftFront"));
        leftBack = MotorEx.Companion.rev12to1(hardwareMap.get(DcMotorEx.class, "leftBack"));
        rightBack = MotorEx.Companion.rev12to1(hardwareMap.get(DcMotorEx.class, "rightBack"));
        rightFront = MotorEx.Companion.rev12to1(hardwareMap.get(DcMotorEx.class, "rightFront"));


        leftFront.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        leftBack.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        rightBack.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);
        rightFront.setZeroPowerBehavior(DcMotor.ZeroPowerBehavior.BRAKE);

        // TODO: reverse motor directions if needed
        //   leftFront.setDirection(DcMotorSimple.Direction.REVERSE);
        rightBack.setDirection(DcMotor.Direction.REVERSE);
        rightFront.setDirection(DcMotor.Direction.REVERSE);

        // TODO: make sure your config has an IMU with this name (can be BNO or BHI)
